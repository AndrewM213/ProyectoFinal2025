    private void configurarTabla(){
        this.dtm = new javax.swing.table.DefaultTableModel();
        this.dtm.setColumnIdentifiers(new Object[]{"ID","Nombre","Telefono","Email"});
        jtblProveedor.setModel(this.dtm);
    }
    
    private void actualizarTabla(){
        if(this.dtm != null){
            this.dtm.setRowCount(0);
            
            if(gestorInventario != null && gestorInventario.getListaProveedor() !=null){
                for(Proveedor p : gestorInventario.getListaProveedor()){
                    Object[] fila ={
                        p.getIDproveedor(),
                        p.getNombre(),
                        p.getTelefono(),
                        p.getEmail(),
                    };
                    this.dtm.addRow(fila);
                }
            }
        }
    }
    
    

