    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            String nombre = txtNombre.getText();
            int telefono = Integer.parseInt(txtTelefono.getText());
            String email = txtEmail.getText();
            
            int nuevoId = gestorInventario.getListaProveedor().size() + 1;
            Proveedor nuevoProv = new Proveedor(nuevoId, nombre, telefono, email);
            gestorInventario.agregarProveedor(nuevoProv);
            
            actualizarTabla();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Proveedor guardado con exito. ");
            
        }catch (NumberFormatException e ){
            JOptionPane.showMessageDialog(this, "Error telefono debe ser un numero. ", "Error", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        limpiarCampos();
    }//GEN-LAST:event_btnLimpiarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = jtblProveedor.getSelectedRow();
        if (fila >= 0){
            int confirm = JOptionPane.showConfirmDialog(this, "Â¿Seguro que deseas eliminar al Proveedor?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION){
                int id = (int) jtblProveedor.getValueAt(fila, 0);
                
                gestorInventario.eliminarProveedor(id);
                
                actualizarTabla();
                JOptionPane.showMessageDialog(this, "Proveedor eliminado.");
            }
        }else {
            JOptionPane.showMessageDialog(this, "Seleccione una fila primero.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

